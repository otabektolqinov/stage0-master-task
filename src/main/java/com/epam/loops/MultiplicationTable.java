package com.epam.loops;

public class MultiplicationTable {

    public void printTable(int numberTableToPrint) {
        System.out.println("1 x " +  numberTableToPrint + " = " + (numberTableToPrint));
        System.out.println("2 x " +  numberTableToPrint + " = " + (numberTableToPrint * 2));
        System.out.println("3 x " +  numberTableToPrint + " = " + (numberTableToPrint * 3));
        System.out.println("4 x " +  numberTableToPrint + " = " + (numberTableToPrint * 4));
        System.out.println("5 x " +  numberTableToPrint + " = " + (numberTableToPrint * 5));
        System.out.println("6 x " +  numberTableToPrint + " = " + (numberTableToPrint * 6));
        System.out.println("7 x " +  numberTableToPrint + " = " + (numberTableToPrint * 7));
        System.out.println("8 x " +  numberTableToPrint + " = " + (numberTableToPrint * 8));
        System.out.println("9 x " +  numberTableToPrint + " = " + (numberTableToPrint * 9));
        System.out.println("10 x " +  numberTableToPrint + " = " + (numberTableToPrint * 10));
    }

}
